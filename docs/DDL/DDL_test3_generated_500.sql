-- 2) 500件揃っていなければ例外で止める（中途半端な成功を防ぐ）
DO $$
DECLARE
  v_company_id uuid;
  v_expected int := 500;
  v_actual int;
  v_missing_count int;
  v_missing_sample text;
BEGIN
  SELECT p.company_id INTO v_company_id
  FROM seed_params_test3_gen p
  LIMIT 1;

  SELECT COUNT(*) INTO v_actual
  FROM employees e
  JOIN departments d ON d.id = e.department_id
  WHERE d.company_id = v_company_id
    AND e.employee_code ~ '^[0-9]+$'
    AND e.employee_code::int BETWEEN 2700001 AND 2700500;

  IF v_actual < v_expected THEN
    SELECT
      COUNT(*)::int,
      COALESCE(string_agg(code::text, ', ' ORDER BY code), '')
    INTO v_missing_count, v_missing_sample
    FROM (
      SELECT gs.code
      FROM (SELECT generate_series(2700001, 2700500) AS code) gs
      LEFT JOIN (
        SELECT DISTINCT e.employee_code::int AS code
        FROM employees e
        JOIN departments d ON d.id = e.department_id
        WHERE d.company_id = v_company_id
          AND e.employee_code ~ '^[0-9]+$'
          AND e.employee_code::int BETWEEN 2700001 AND 2700500
      ) ex ON ex.code = gs.code
      WHERE ex.code IS NULL
      ORDER BY gs.code
      LIMIT 20
    ) m;

    RAISE EXCEPTION 'DDL_test3 seed is incomplete: expected % employees (codes 2700001-2700500) but got %. missing=% (sample: %). Likely causes: the SQL was partially executed/truncated, or DB has an unexpected UNIQUE constraint and rows were skipped by ON CONFLICT.',
      v_expected, v_actual, v_missing_count, v_missing_sample;
  END IF;
END $$;

